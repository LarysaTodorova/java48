package practice.calculator;

public class Calculator {

    int x;
    int y;


    public int subtraction() {
        int result = x-y;
        return result;

    }

    public int sum() {
        int result = x+y;
        return result;

    }

    public int multi(){
        int result = x*y;
        return result;


    }

    public double div(){
        double result = x/y;
        return result;


    }


}
