package practice.calculatorWithArguments;

public class CalculatorWithArguments {

    public int sum( int x,int y ){
        int result = x+y;
        return result;


    }

    public int multi( int x, int y){
        int result = x*y;
        return result;


    }

    public int subtraction( int x, int y) {
        int result = x - y;
        return result;

    }

    public double div( double x, double y) {
        double result = x/y;
        return result;


    }

}
