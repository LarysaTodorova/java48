package practice.supperCalc;

public class SupperCalc {

    public double sum(double x, double y){
        double result = x + y;
        return result;

    }

    public double multi(double x, double y){
        double result = x * y;
        return result;

    }

    public double sub(double x, double y){
        double result = x - y;
        return result;

    }

    public double div(double x, double y){
        double result = x / y;
        return result;

    }

}
